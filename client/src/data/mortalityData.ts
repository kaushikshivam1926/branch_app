// Helper function to get mortality rate for a given age
export function getMortalityRate(age: number): number {
  // Clamp age between 0 and 115
  const clampedAge = Math.max(0, Math.min(115, Math.floor(age)));
  return mortalityRates[clampedAge] || 0;
}

// Helper function to get smoking scaling factor for a given age
export function getSmokingScalingFactor(age: number, isSmoker: boolean): number {
  if (!isSmoker) return 1.0;
  
  // Clamp age between 15 and 100
  const clampedAge = Math.max(15, Math.min(100, Math.floor(age)));
  return smokingScalingFactors[clampedAge] || 1.0;
}

// Calculate annual insurance premium
// Formula: Annual Premium = Sum Assured × Mortality Rate × Risk Multiplier
export function calculateInsurancePremium(
  sumAssured: number,
  age: number,
  isSmoker: boolean = false
): number {
  const mortalityRate = getMortalityRate(age);
  const riskMultiplier = getSmokingScalingFactor(age, isSmoker);
  
  return sumAssured * mortalityRate * riskMultiplier;
}
